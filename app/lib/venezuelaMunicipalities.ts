const municipalitiesByState: Record<
  string,
  Array<{ value: string; label: string; latLng: [number, number] }>
> = Object.fromEntries(
  Object.entries(rawMunicipalitiesByState).map(([state, names]) => [
    state,
    names.map((name) => {
      const normalizedName = toValue(name);
      const val = municipalityValueOverrides[state]?.[normalizedName] ?? normalizedName;
      const specificLatLng = municipalityCoords[state]?.[val];
      return {
        value: val,
        label: name,
        latLng: specificLatLng ?? stateLatLngMap[state],
      };
    }),
  ])
);

export const getMunicipalitiesByState = (stateValue: string) => {
  return municipalitiesByState[stateValue] ?? [];
};

export const getDefaultMunicipalityByState = (stateValue: string) => {
  return getMunicipalitiesByState(stateValue)[0] ?? null;
};

export const getMunicipalityByValue = (
  stateValue: string,
  municipalityValue: string
) => {
  return getMunicipalitiesByState(stateValue).find(
    (item) => item.value === municipalityValue
  );
};

export const useVenezuelaMunicipalities = () => {
  return {
    getMunicipalitiesByState,
    getDefaultMunicipalityByState,
    getMunicipalityByValue,
  };
};
